#leap years occur in every year that is evenly
#divisible by 4, unless the year is divisible by 100
#if divisible by 100, then not  aleap year unless year is
#evenly divisible by 400

def leap_year?(year)
  if year % 4 == 0
    true
  elsif year % 100 == 0
    true if year % 400 == 0
  end
end

